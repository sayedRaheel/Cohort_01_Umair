a = 5
print(type(a))

b = 3.14

print(type(b))

c = 'Hi there!'

print(type(c))

d = {'name': 'Umair' , 'age': 28 }

print(type(d))

e = [1,2,3,4,5]

print(type(e))

f = (10,20,30)

print(type(f))

h = range(1,11)

print(type(h))

i = set([1,2,3,4,5])

print(type(i))

j = frozenset([6,7,8,9,10])

print(type(j))

